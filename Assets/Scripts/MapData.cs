    private static Dictionary<(int, int), float> doorStates = new Dictionary<(int, int), float>();

    public static int currentLevel = 1;

    public static int[,] GetCurrentMap()
    {
        switch (currentLevel)
        {
            case 2: return map2;
            case 3: return map3;
            case 4: return map4;
            case 5: return map5;
            default: return map;
        }
    }

    public static int GetWallType(int x, int y)
    {
        var m = GetCurrentMap();
        if (x < 0 || y < 0 || y >= m.GetLength(0) || x >= m.GetLength(1)) return 1;
        return m[y, x];
    }
    
    public static bool IsDoor(int x, int y) => GetWallType(x, y) == 2;

    public static bool IsWall(int x, int y)
    {
        int tile = GetWallType(x, y);
        if (tile == 2) return GetDoorOpen(x, y) < 0.8f;
        return tile != 0;
    }

    public static float GetDoorOpen(int x, int y)
    {
        if (doorStates.TryGetValue((x, y), out float val)) return val;
        return 0f;
    }

    public static void ActivateDoor(int x, int y)
    {
        if (IsDoor(x, y) && GetDoorOpen(x, y) < 0.1f) doorStates[(x, y)] = 0.01f;
    }

    public static void UpdateDoors()
    {
        List<(int, int)> keys = new List<(int, int)>(doorStates.Keys);
        foreach (var key in keys)
        {
            if (doorStates[key] > 0 && doorStates[key] < 1f)
            {
                doorStates[key] += Time.deltaTime * 1.2f;
                if (doorStates[key] >= 1f) doorStates[key] = 1f;
            }
        }
    }

    public static void Reset()
    {
        doorStates.Clear();
    }

    public static Dictionary<(int, int), float> GetDoorStates()
    {
        return doorStates;
    }

    public static void SetDoorState(int x, int y, float value)
    {
        doorStates[(x, y)] = value;
    }

    public static bool IsLever(int x, int y)
    {
        int tile = GetWallType(x, y);
        return tile == 6;
    }
